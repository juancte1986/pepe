package ar.edu.uces.progweb2.agenda.helper;

public class EventHelper {
	
	private int num;
	private int top;
	
	public EventHelper(int num, int top){
		this.num = num;
		this.top = top;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public int getTop() {
		return top;
	}
	public void setTop(int top) {
		this.top = top;
	}

}
